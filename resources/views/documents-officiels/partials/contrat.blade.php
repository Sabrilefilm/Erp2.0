    <p class="font-semibold text-white">CONTRAT OFFICIEL DE PRESTATION – UNIONS AGENCY</p>
    <p>Entre : <strong class="text-white">UNIONS AGENCY</strong>, agence spécialisée dans l'accompagnement de créateurs TikTok Live, représentée par le responsable désigné, ci-après dénommée « L'Agence », et le créateur (nom, pseudo TikTok, email, téléphone), ci-après dénommé « Le Créateur ».</p>
    <section>
        <h2 class="text-base font-bold text-white uppercase mb-2">Article 1 – Objet</h2>
        <p>Le présent contrat encadre la collaboration entre L'Agence et le Créateur pour le développement de son activité TikTok Live.</p>
    </section>
    <section>
        <h2 class="text-base font-bold text-white uppercase mb-2">Article 2 – Engagements du créateur</h2>
        <ul class="list-disc pl-5 space-y-1">
            <li>Effectuer minimum 7 jours de live par mois.</li>
            <li>Réaliser minimum 16 heures de live par mois.</li>
            <li>Posséder un seul compte TikTok affilié à l'agence.</li>
            <li>Respecter la stratégie live et contenu définie par l'agence.</li>
            <li>Respecter le règlement intérieur officiel de l'agence.</li>
            <li>Respecter strictement les règles TikTok.</li>
        </ul>
    </section>
    <section>
        <h2 class="text-base font-bold text-white uppercase mb-2">Article 3 – Rémunération</h2>
        <p>L'Agence percevra une commission sur les revenus générés via TikTok (diamants, cadeaux, partenariats négociés).</p>
    </section>
    <section>
        <h2 class="text-base font-bold text-white uppercase mb-2">Article 4 – Durée et préavis</h2>
        <p>Le contrat est conclu pour une durée indéterminée avec un préavis obligatoire de 90 jours en cas de résiliation par le Créateur. Conformément au droit de rétractation légal, le Créateur dispose d'un délai de 15 jours à compter de la signature pour quitter l'agence sans préavis.</p>
    </section>
    <section>
        <h2 class="text-base font-bold text-white uppercase mb-2">Article 5 – Résiliation immédiate</h2>
        <p>L'Agence peut résilier le contrat à tout moment en cas de non-respect des consignes, du règlement intérieur ou des règles TikTok. En cas de bannissement, suspension ou décision de TikTok impactant la collaboration, le contrat pourra être résilié immédiatement.</p>
    </section>
    <section>
        <h2 class="text-base font-bold text-white uppercase mb-2">Article 6 – Exclusivité</h2>
        <p>Le Créateur s'engage à ne pas collaborer avec une autre agence TikTok pendant la durée du contrat sans accord écrit de L'Agence.</p>
    </section>
    <section>
        <h2 class="text-base font-bold text-white uppercase mb-2">Article 7 – Sanctions</h2>
        <p>En cas de manquement : avertissement, perte de récompenses, suspension interne ou exclusion définitive pourront être appliqués.</p>
    </section>
