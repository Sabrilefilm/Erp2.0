<section>
    <h2 class="text-base font-bold text-white uppercase mb-2">Objectif de l'agence</h2>
    <p class="mb-2">L'agence a pour mission de permettre aux créateurs de :</p>
    <ul class="list-disc pl-5 space-y-1">
        <li>Créer du contenu de qualité et divertissant</li>
        <li>Progresser dans leurs lives et leur visibilité</li>
        <li>Respecter un cadre sécurisé et professionnel</li>
    </ul>
    <p class="mt-3 font-medium">Tout comportement contraire à cet objectif sera sanctionné selon les règles ci-dessous.</p>
</section>

<hr class="border-slate-600">

<section>
    <h2 class="text-base font-bold text-white uppercase mb-2">Lives : contenu autorisé et interdit</h2>
    <ul class="list-disc pl-5 space-y-1 mb-2">
        <li>Les lives servent uniquement à créer du contenu engageant et divertissant.</li>
    </ul>
    <p class="font-semibold mb-1">Interdiction totale de :</p>
    <ul class="list-disc pl-5 space-y-1 mb-2">
        <li>Régler des comptes ou créer du drama</li>
        <li>Lives déplacés ou choquants</li>
        <li>Lives « dodo » (inactifs)</li>
        <li>Lives célibataire sans concept</li>
        <li>Contenu sexuel explicite ou provocant pouvant entraîner un bannissement TikTok</li>
    </ul>
    <p class="font-medium text-amber-300/90">Sanctions : rappel immédiat, annulation match off, perte de récompenses, exclusion en cas de répétition.</p>
</section>

<hr class="border-slate-600">

<section>
    <h2 class="text-base font-bold text-white uppercase mb-2">3 – Plateforme officielle & groupes</h2>
    <ul class="list-disc pl-5 space-y-1">
        <li>WhatsApp est la seule plateforme autorisée pour gérer les problèmes.</li>
        <li>Interdiction totale d'utiliser : TikTok, autres réseaux ou groupes externes.</li>
        <li>Aucun autre groupe n'est autorisé que les groupes officiels de l'agence.</li>
    </ul>
</section>

<hr class="border-slate-600">

<section>
    <h2 class="text-base font-bold text-white uppercase mb-2">4 – Communication et sujets interdits</h2>
    <ul class="list-disc pl-5 space-y-1">
        <li>Interdiction de parler de : politique, religion, sujets sensibles</li>
        <li>Interdiction de démarcher sur TikTok un membre de l'agence pour promotion ou recrutement</li>
    </ul>
</section>

<hr class="border-slate-600">

<section>
    <h2 class="text-base font-bold text-white uppercase mb-2">5 – Tenue et image pendant les lives</h2>
    <ul class="list-disc pl-5 space-y-1 mb-2">
        <li><strong>Liberté d'expression :</strong> chaque créateur peut s'habiller comme il/elle le souhaite</li>
        <li><strong>Limites :</strong> éviter tout ce qui peut entraîner un bannissement TikTok (nudité, contenu sexuel explicite)</li>
        <li><strong>Responsabilité :</strong> chaque créateur est responsable de son image</li>
        <li>L'agent ou la manageuse peut prévenir ou interrompre un live si nécessaire</li>
    </ul>
</section>

<hr class="border-slate-600">

<section>
    <h2 class="text-base font-bold text-white uppercase mb-2">6 – Rôle des créateurs</h2>
    <ul class="list-disc pl-5 space-y-1 mb-2">
        <li>Créer du contenu et divertir</li>
        <li>Participer à 1 match off officiel par mois</li>
        <li>Suivre les directives de l'agent</li>
        <li>Participer à la réunion mensuelle obligatoire</li>
        <li>Respecter les règles TikTok et celles de l'agence</li>
    </ul>
    <p class="font-semibold mb-1">Non participation à la réunion :</p>
    <ul class="list-disc pl-5 space-y-1">
        <li>Exclusion des nouvelles directives du mois</li>
        <li>Toute erreur ou incident sera à la charge du créateur</li>
    </ul>
</section>

<hr class="border-slate-600">

<section>
    <h2 class="text-base font-bold text-white uppercase mb-2">7 – Rôle des agents</h2>
    <ul class="list-disc pl-5 space-y-1 mb-2">
        <li>Gérer et accompagner les créateurs</li>
        <li>Planifier les matchs et événements</li>
        <li>Gérer les petits soucis et remonter les problèmes graves à la manageuse</li>
        <li>Fournir ses coordonnées et @TikTok aux créateurs</li>
        <li>Former les sous-agents et nouveaux créateurs</li>
    </ul>
    <p class="font-semibold mb-1">Procédure en cas de conflit :</p>
    <ol class="list-decimal pl-5 space-y-1">
        <li>Agent collecte infos auprès du créateur</li>
        <li>Transmet à la manageuse</li>
        <li>Si nécessaire, la manageuse peut contacter le créateur directement</li>
        <li>Vérification et validation avec l'agent</li>
        <li>Communication aux parties et information au fondateur</li>
    </ol>
</section>

<hr class="border-slate-600">

<section>
    <h2 class="text-base font-bold text-white uppercase mb-2">8 – Rôle de la manageuse</h2>
    <ul class="list-disc pl-5 space-y-1">
        <li>Résoudre les problèmes dans un délai de 24–48h</li>
        <li>Encadrer agents et sous-agents</li>
        <li>Appliquer les sanctions et pénalités</li>
        <li>Superviser les match off et les lives</li>
        <li>Organiser 1 réunion mensuelle obligatoire</li>
        <li>Valider tous les transferts de créateurs avec les agents concernés et informer le fondateur</li>
    </ul>
</section>

<hr class="border-slate-600">

<section>
    <h2 class="text-base font-bold text-white uppercase mb-2">9 – Rôle du fondateur</h2>
    <ul class="list-disc pl-5 space-y-1">
        <li>Décisions finales sur conflits et sanctions lourdes</li>
        <li>Développement de l'agence et validation des projets</li>
        <li>Supervision des transferts et validation légale</li>
    </ul>
</section>

<hr class="border-slate-600">

<section>
    <h2 class="text-base font-bold text-white uppercase mb-2">10 – Réunions et transferts</h2>
    <ul class="list-disc pl-5 space-y-1">
        <li>Créateurs doivent participer à 1 réunion mensuelle</li>
        <li>Transferts décidés par agent et manageuse, validés par le fondateur</li>
        <li>Si créateur refuse ou aucun agent disponible : créateur transféré ou laissé seul</li>
        <li><strong>Créateur seul :</strong> peut uniquement envoyer des requêtes par e-mail</li>
    </ul>
    <p class="mt-2 font-semibold text-white">Unions Agency</p>
</section>

<hr class="border-slate-600">

<section>
    <h2 class="text-base font-bold text-white uppercase mb-2">11 – Récompenses et nouvelles recrues</h2>
    <ul class="list-disc pl-5 space-y-1">
        <li><strong>Validation :</strong> 7 jours de live, 16h minimum, 1000 diamants, WhatsApp obligatoire</li>
        <li>Récompenses selon rôle (créateur, agent, manager)</li>
        <li><strong>Limites :</strong> récompenses pour 1–10 nouveaux créateurs disponibles immédiatement, au-delà : attente du 1er du mois</li>
        <li>Créateurs exclus ou arrêtés : récompenses reversées à l'agence</li>
    </ul>
</section>

<hr class="border-slate-600">

<section>
    <h2 class="text-base font-bold text-white uppercase mb-2">12 – Sous-agent</h2>
    <ul class="list-disc pl-5 space-y-1">
        <li>Aide les agents</li>
        <li>Rappeler le règlement et programmer des matchs</li>
        <li>Ne prend aucune décision officielle</li>
        <li>Elle aide gratuitement les agents et l'agent peut donner une partie de ses récompenses à sa bonne foi</li>
    </ul>
</section>

<hr class="border-slate-600">

<section>
    <h2 class="text-base font-bold text-white uppercase mb-2">13 – Statistiques et images</h2>
    <ul class="list-disc pl-5 space-y-1">
        <li>Usage interdit d'images / statistiques hors règles</li>
        <li><strong>Sanctions :</strong> perte de diamants 3 mois · exclusion définitive</li>
    </ul>
</section>

<hr class="border-slate-600">

<section>
    <h2 class="text-base font-bold text-white uppercase mb-2">14 – Week-ends et disponibilité</h2>
    <ul class="list-disc pl-5 space-y-1">
        <li>Fondateur et manageuse en off sauf urgence</li>
        <li>Agents peuvent gérer si nécessaire</li>
    </ul>
</section>

<hr class="border-slate-600">

<section>
    <h2 class="text-base font-bold text-white uppercase mb-2">15 – Vie privée</h2>
    <ul class="list-disc pl-5 space-y-1">
        <li>Interdiction de partager Snapchat ou photos privées</li>
        <li>Numéros entre créateurs : accord obligatoire</li>
        <li>Infraction : retrait WhatsApp et communication par email 3 mois</li>
    </ul>
</section>
